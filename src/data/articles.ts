// Helper function to get related articles based on tags and category
export function getRelatedArticles(currentArticle: Article, allArticles: Article[], limit: number = 4): Article[] {
  const related = allArticles
    .filter(article => article.id !== currentArticle.id)
    .map(article => {
      let score = 0;
      
      // Same category gets higher score
      if (article.category === currentArticle.category) {
        score += 3;
      }
      
      // Shared tags get points
      const sharedTags = article.tags.filter(tag => currentArticle.tags.includes(tag));
      score += sharedTags.length * 2;
      
      // Trending articles get a small boost
      if (article.trending) {
        score += 1;
      }
      
      return { article, score };
    })
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
    .map(item => item.article);
    
  return related;
}